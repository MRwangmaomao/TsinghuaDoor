extern u8 OLED_GRAM_Map[128][64];	//存放移动数组
extern  Gamemove_t Gamemove;
Game_state_t Game_state;

void turtle_evil(){
	int key=0;
	u8 break_flag = 0;
	int a=0;
	int b=0;
	int c=0;
	u32 d=0;
	int e=0; 
	int f=0;
	int g=0;
	int h=0;
	int z;
	int y;
	int flag=0;
	Game_State_Init();
	OLED_CLS();//清屏
	confirm();
	delay_ms(2);
  z=OLED_GRAM_Map[120][44];
	y=Gamemove.state;
	
	while(1)
	{	
		key = KEY01_Scan();
		//存活状态
		if(Gamemove.dead==0)
		{
			if(Game_state.page==0)
			{
				OLED_DrawBMP(0,0,128,7,(unsigned char *)BMP1);//封面
				OLED_P6x8Str(20,7,"Press Play Key");
				Game_state.page=1;
				delay_ms(500);
			}
			 
			if(key==KEY0_PRES&&Game_state.page==1)
			{
				a++;
				if(a==1) OLED_CLS();//清屏
				OLED_DrawBMP(0,0,16,2,(unsigned char *)BMP3);//师傅头像
				OLED_P6x8Str(17,0,"Now evil is in the");
				OLED_P6x8Str(17,1,"air.And U,my deer");
				OLED_P6x8Str(17,2,"student,it's time");
				OLED_P6x8Str(17,3,"to  destory  the");
				OLED_P6x8Str(17,4,"Energy Pearl, and");
				OLED_P6x8Str(17,5,"save the people!");
				OLED_P6x8Str(17,6,"It's a dangerous");
				OLED_P6x8Str(17,7,"task.Good Luck!");
				Game_state.page=2;
			}
		
			if(key==KEYUP_PRES&&Game_state.page==2)
			{
				b++;
				if(b==1) OLED_CLS();//清屏
				OLED_DrawBMP(112,1,128,3,(unsigned char *)BMP4);//徒弟头像
				OLED_P6x8Str(45,1,"Master REN");
				OLED_P6x8Str(2,2,"I will try my best");
				OLED_DrawBMP(0,4,16,6,(unsigned char *)BMP3);//师傅头像
				OLED_P6x8Str(17,4,"Take Care");
				OLED_DrawBMP(112,5,128,7,(unsigned char *)BMP4);//徒弟头像
				OLED_P6x8Str(55,6,"Take Care");
				Game_state.page=3;

			}
			
			if(key==KEYUP_PRES&&Game_state.page==3)
			{
				c++;
				if(c==1)
				{
					OLED_CLS();//清屏
					Set_Start_Point(64,5);
					OLED_DrawPoint(64,5,1,1);
					Show_GameFrame();
					Game_state.page=4;
				}
			}
		
			if(Game_state.page==4)
			{
				d++;
				move2(key);
				check_State();
				OLED_P6x8Str(1,7,"P");
			
				OLED_P6x8Num(8,7,Gamemove.x);
				OLED_P6x8Num(28,7,Gamemove.y);
				
				if(d%30==0&&d%60!=0)  OLED_DrawTrap1();
				if(d%30==0&&d%60==0)  OLED_EraseTrap1();
					
				if(d%55==0&&d%110!=0) OLED_DrawTrap2();
				if(d%55==0&&d%110==0) OLED_EraseTrap2();
					
				if(d%40==0&&d%80!=0) OLED_DrawTrap3();
				if(d%40==0&&d%80==0) OLED_EraseTrap3();
			 
				if(Gamemove.state == 2)
				{
					Gamemove.dead = 1;
				}
			
				if(Gamemove.state == 3)
				{
					e++;
					if(e==1)
					{
						OLED_DrawPoint(Gamemove.x,Gamemove.y,0,0);	
						Set_Start_Point(6,42);
						flag=1;
					}
				}
			
				if(Gamemove.state == 5)
				{
					f++;
					if(f==1)
					{	
						OLED_DrawPoint(Gamemove.x,Gamemove.y,0,0);	
						Set_Start_Point(64,5);
						flag=0;
					}
				}
			
				if(Gamemove.state == 4)
				{
					g++;
					check_State();
					if(g==1)
					{
						OLED_DrawPoint(Gamemove.x,Gamemove.y,0,0);	
						Set_Start_Point(70,51);
						flag=2;
					}
				}

				if(flag==1) OLED_P6x8Str(100,7,"Maze");
				if(flag==0) OLED_P6x8Str(100,7,"Base");
				if(flag==2) 
				{
					OLED_P6x8Str(94,6,"Laser");
					OLED_P6x8Str(97,7,"Trap");
				}
			} 
		 }
 
		 //游戏结束
		 if(Gamemove.dead==1)
		 {
			 e++;
			 if(e==1)
			 {
				 OLED_CLS();//清屏
				 OLED_P8x16Str(5,5,"GAME OVER");
				 OLED_DrawBMP(0,0,128,4,(unsigned char *)BMP5);
				 OLED_P6x8Str(112,6,"by");
				 OLED_P6x8Str(100,7,"Sine");
			 } 
		 }
 
		//游戏胜利
		if(Gamemove.state == 6)
		{
			h++;
			if(h==1)
			{
				Gamemove.dead=3;
				OLED_CLS();//清屏	
			}
			OLED_DrawBMP(0,0,128,4,(unsigned char *)BMP5);
			OLED_P8x16Str(35,6,"YOU WIN");
			OLED_P8x16Str(112,6,"by");
			OLED_P8x16Str(100,7,"Sine");
			delay_ms(500);
			delay_ms(500);
			break_flag = 1;
		}
		
		if(key == KEY1_PRES)
			break_flag = 1;
		
		if(break_flag)
			break;
	}
}


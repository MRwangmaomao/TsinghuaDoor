//输出汉字字符串
void OLED_P14x16Str(u8 x,u8 y,u8 ch[])
{
    u8 wm=0,ii = 0;
    u16 adder=1; 
    
    while(ch[ii] != '\0')
    {
    wm = 0;
    adder = 1;
    while(F14x16_Idx[wm] > 127)
    {
        if(F14x16_Idx[wm] == ch[ii])
        {
            if(F14x16_Idx[wm + 1] == ch[ii + 1])
            {
                    adder = wm * 14;
                    break;
            }
        }
        wm += 2;			
    }
    if(x>118){x=0;y++;}
    OLED_Set_Pos(x , y); 
    if(adder != 1)// 显示汉字					
    {
        OLED_Set_Pos(x , y);
        for(wm = 0;wm < 14;wm++)               
        {
                OLED_WrDat(F14x16[adder]);	
                adder += 1;
        }      
        OLED_Set_Pos(x,y + 1); 
        for(wm = 0;wm < 14;wm++)          
        {
                OLED_WrDat(F14x16[adder]);
                adder += 1;
        }   		
    }
    else			  //显示空白字符			
    {
        ii += 1;
        OLED_Set_Pos(x,y);
        for(wm = 0;wm < 16;wm++)
        {
                        OLED_WrDat(0);
        }
        OLED_Set_Pos(x,y + 1);
        for(wm = 0;wm < 16;wm++)
        {   		
                        OLED_WrDat(0);	
        }
    }
        x += 14;
        ii += 2;
    }
}


//输出汉字和字符混合字符串
void OLED_Print(u8 x, u8 y, u8 ch[])
{
    u8 ch2[3];
    u8 ii=0;        
    while(ch[ii] != '\0')
    {
        if(ch[ii] > 127)
        {
            ch2[0] = ch[ii];
            ch2[1] = ch[ii + 1];
            ch2[2] = '\0';			//汉字为两个字节
            OLED_P14x16Str(x , y, ch2);	//显示汉字
            x += 14;
            ii += 2;
        }
        else
        {
            ch2[0] = ch[ii];	
            ch2[1] = '\0';			//字母占一个字节
            OLED_P8x16Str(x , y , ch2);	//显示字母
            x += 8;
            ii+= 1;
        }
    }
} 


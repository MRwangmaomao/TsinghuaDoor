/*!
 *  @brief      绘制一个点（x,y）
 *  @param      x      横坐标 x的范围0～127
 *  @param      y      纵坐标 y的范围0～64
 *  Sample usage:       OLED_PutPixel(5,5);    
 */
void OLED_PutPixel(u8 x,u8 y)
{
	u8 data1;  //data1当前点的数据 
	 
  OLED_Set_Pos(x,y); 
	data1 = 0x01<<(y%8); 	
	OLED_WrCmd(0xb0+(y>>3));
	OLED_WrCmd(((x&0xf0)>>4)|0x10);
	OLED_WrCmd((x&0x0f)|0x00);
	OLED_WrDat(data1); 	 	
}

 

/*!
 *  @brief      绘制一个点（x,y）
 *  @param      x      横坐标 x的范围0～127
 *  @param      y      纵坐标 y的范围0～64
 *  @param      show     显示结果
 */ 
void inf_lcd_draw_pixel(u8 x, u8 y, u8 show)
{
	uint8_t pos = 0;
	uint8_t bx = 0;
	uint8_t temp = 0; 

	if (x > 127 || y > 64)
	{
			return;
	}

	pos = 7 - y / 8;
	bx = y % 8;
	temp = 1 << (7 - bx);
	
	if (show)
	{
			OLED_GRAM[x][pos] |= temp; //对应位置为1
	}
	else
	{
			OLED_GRAM[x][pos] &= ~temp;
	}       
  	oled_set_pos(x,y);	
	OLED_WrDat(OLED_GRAM[x][pos]); 	 //设置列的显存
}

